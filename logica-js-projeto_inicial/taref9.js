let dia = prompt ("Digite um número de 1 a 7 para saber qual o dia da semana");
switch (dia){
    case "1":
        alert ("Hoje é Domingo!");
    break;
    case "2":
        alert ("Hoje é Segunda-Feira!");
    break;
    case "3":
        alert ("Hoje é Terça-feira!");
    break;
    case "4":
        alert ("Hoje é Quarta-Feira!");
    break;
    case "5":
        alert ("Hoje é Quinta-Feira!")
    break;
    case "6":
        alert ("Hoje é Sexta-Feira!");
    break;
    case "7":
        alert ("Hoje é Sabádo!");
    break;
    default:
        alert ("Valor Inválido!!!");
}
 